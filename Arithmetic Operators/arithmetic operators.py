
a = 20
b= 10
print(a+b)    # This is an addition operator that adds two numbers and returns the result
print(a-b)     # This is a subtraction operator that subtracts one number from another and returns the result
print(a*b)     # This is a multiplication operator that multiplies two numbers and returns the result
print(a/b)     # This is a division operator that divides one number by another and returns the result
print(a%b)     # This is a modulus operator that returns the remainder of a division operation      
print(a//b)    # This is a floor division operator that divides one number by another and returns the largest integer less than or equal to the result
print(a**b)    # This is an exponentiation operator that raises a number to the power of another number and returns the result
