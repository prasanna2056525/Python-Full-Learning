print(10+20)    # This is an addition operator that adds two numbers and returns the result
print(10-5)     # This is a subtraction operator that subtracts one number from another and returns the result
print(10*5)     # This is a multiplication operator that multiplies two numbers and returns the result
print(10/2)     # This is a division operator that divides one number by another and returns the result
print(10%3)     # This is a modulus operator that returns the remainder of a division operation
print(10//3)    # This is a floor division operator that divides one number by another and returns the largest integer less than or equal to the result
print(10**2)    # This is an exponentiation operator that raises a number to the power of another number and returns the result     

