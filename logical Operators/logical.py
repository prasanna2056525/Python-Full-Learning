
print( 10 > 5 and 10 < 20) # This is a greater than operator that compares two values and returns True if the left value is greater than the right value, otherwise it returns False

print( 10 > 5 or 10 < 20) # This is a logical OR operator that compares two values and returns True if at least one of the values is True, otherwise it returns False
print( not 10 > 5) # This is a logical NOT operator that negates the value of a boolean expression and returns True if the expression is False, and False if the expression is True

marks = int(input("Enter your marks: "))

print(marks > 90 or marks <= 100) # This is a logical AND operator that checks if the marks are greater than 90 and less than or equal to 100, and returns True if both conditions are satisfied, otherwise it returns False

